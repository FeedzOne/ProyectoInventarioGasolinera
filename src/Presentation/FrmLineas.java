    private void SaveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBTNActionPerformed
        // TODO add your handling code here:
        
        //LINE FIELD
        if(LineField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Line is required", "Validation", JOptionPane.WARNING_MESSAGE); 
            LineField.requestFocusInWindow();
            return;
        }

         String msg = null;
        if(IDField.getText().equals("")){
            LLines fn = new LLines();
            DLines dts = new DLines();
            
            dts.setLine(LineField.getText());
            msg = fn.insertLines(dts);
            if(msg == "si"){
               JOptionPane.showMessageDialog(rootPane, "Successfully Registered", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }else
            {
                JOptionPane.showMessageDialog(rootPane, "Uh oh, an error has occurred", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }
        }else{
            LLines fn = new LLines();
            DLines dts = new DLines();

            dts.setID(Integer.parseInt(IDField.getText()));
            dts.setLine(LineField.getText());
            msg = fn.editLines(dts);
            if(msg == "si"){
               JOptionPane.showMessageDialog(rootPane, "Successfully Updated", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }else
            {
                JOptionPane.showMessageDialog(rootPane, "Uh oh, an error has occurred", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }
        }
        cleanfield();
        enablebtn(false);
        showSearch("");
    }//GEN-LAST:event_SaveBTNActionPerformed

