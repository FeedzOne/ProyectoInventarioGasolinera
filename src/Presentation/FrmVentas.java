    public void showProducts(String product) {
        //System.out.println("Dio Enter "+ product);
        LStorage fn = new LStorage();
        DStorage dts = new DStorage();
        DefaultTableModel tblData = (DefaultTableModel) tblSales.getModel();
        dts.setStorageID(product);
        String reg[] = fn.searchshowProducts(dts);
        if (reg[0] != null) {
            tblData.insertRow(0, reg);
            tblSales.setModel(tblData);
        } else {
            reg[0] = EnterField.getText();
            reg[1] = "";
            reg[2] = "1";
            reg[3] = "";
            reg[4] = "0.00";
            reg[5] = "0.00";
            tblData.insertRow(0, reg);
            tblSales.setModel(tblData);
        }
    }

