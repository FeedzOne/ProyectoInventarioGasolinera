    private void SaveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBTNActionPerformed
        // TODO add your handling code here:
        if (LineField.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Line is required", "Validation", JOptionPane.WARNING_MESSAGE);
            LineField.requestFocusInWindow();
            return;
        }

        if (SupplierField.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Supplier is required", "Validation", JOptionPane.WARNING_MESSAGE);
            SupplierField.requestFocusInWindow();
            return;
        }
        if (DateField.getDate().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Date is required", "Validation", JOptionPane.WARNING_MESSAGE);
            DateField.requestFocusInWindow();
            return;
        }

        String msg = null, msgfinal = "";

        if (IDField.getText().equals("")) {
            LSales fn = new LSales();
            DSales dts = new DSales();

            dts.setLinesID(Integer.parseInt(LineID.getText()));
            dts.setSupplierID(Integer.parseInt(SupplierID.getText()));

            Calendar cal;
            cal = DateField.getCalendar();
            int aa, mm, dd;
            aa = cal.get(Calendar.YEAR) - 1900;
            mm = cal.get(Calendar.MONTH);
            dd = cal.get(Calendar.DAY_OF_MONTH);

            dts.setEntryDate(new Date(aa, mm, dd));

            //CLEANING FIELDS
            LineField.setText("");
            SupplierField.setText("");
            LineField.setEnabled(false);
            SupplierField.setEnabled(false);

            int idfact = fn.insertSale(dts);
            if (idfact > 0) {
                int filas = tblSales.getRowCount();
                for (int i = 0; i < filas; i++) {
                    String idprod = tblSales.getValueAt(i, 0).toString();
                    String desc = tblSales.getValueAt(i, 1).toString();
                    int qty = Integer.parseInt(tblSales.getValueAt(i, 2).toString());
                    String munit = tblSales.getValueAt(i, 3).toString();
                    double unitprice = Double.parseDouble(tblSales.getValueAt(i, 4).toString());
                    double total = Double.parseDouble(tblSales.getValueAt(i, 4).toString());

                    LBillsDetails fundf = new LBillsDetails();
                    DBillsDetails dtsf = new DBillsDetails();

                    dtsf.setBillsID(idfact);
                    dtsf.setProductID(idprod);
                    dtsf.setQuantity(qty);
                    dtsf.setTotal(total);

                    String msg2 = fundf.RegisterDetailsBills(dtsf);
                    if (msg2.equals("si")) {
                        LStorage funal = new LStorage();
                        DStorage dtsp = new DStorage();

                        dtsp.setStorageID(idprod);
                        dtsp.setDescription(desc);
                        dtsp.setStock(qty);
                        dtsp.setMetricUnit(munit);
                        dtsp.setPrice(unitprice);
                        dtsp.setLinesID(LinesID);

                        msgfinal = funal.insertProducts(dtsp);
                    }
                }
                JOptionPane.showMessageDialog(rootPane, msgfinal, "Info", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Uh oh, an error has occurred", "Info", JOptionPane.INFORMATION_MESSAGE);
            }
            enablebtn(true);
            cleanfield();
        }
    }//GEN-LAST:event_SaveBTNActionPerformed

    public boolean isintable(String text) {
        boolean res = false;
        DecimalFormat ft = new DecimalFormat("####.00");
        for (int i = 0; i < tblSales.getRowCount(); i++) {
            String code = tblSales.getValueAt(i, 0).toString();
            if (code.equals(text)) {
                int quantity = Integer.parseInt(tblSales.getValueAt(i, 2).toString());
                double unitprice = Double.parseDouble(tblSales.getValueAt(i, 4).toString());
                int totalqty = quantity + 1;
                tblSales.setValueAt(totalqty, i, 2);

                double totalpar = unitprice * totalqty;
                tblSales.setValueAt(ft.format(totalpar), i, 5);
                res = true;
            }
        }
        addTotal();
        return res;
    }

    public void addTotal() {
        DecimalFormat ft = new DecimalFormat("####.00");
        double totalp = 0, total = 0;
        for (int i = 0; i < tblSales.getRowCount(); i++) {
            totalp = Double.parseDouble(tblSales.getValueAt(i, 5).toString());
            total += totalp;
            TotalField.setText(ft.format(total));
        }
    }


