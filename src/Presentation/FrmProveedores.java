    private void DeleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBTNActionPerformed
        // TODO add your handling code here:
        if(!IDField.getText().equals("")){
            DSuppliers dts = new DSuppliers();
            LSuppliers fun = new LSuppliers();
            dts.setID(Integer.parseInt(IDField.getText()));
            String msg = fun.deleteSuppliers(dts);
            if(msg.equals("si")){
                JOptionPane.showMessageDialog(rootPane, "Successfully Updated", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Uh oh, an error has occurred", "Info", JOptionPane.ERROR_MESSAGE);  
            }  
        }else{
                JOptionPane.showMessageDialog(rootPane, "Select a Supplier to delete", "Info", JOptionPane.ERROR_MESSAGE); 
    }
        cleanfield();
        enablebtn(false);
        showSearch("");
    }//GEN-LAST:event_DeleteBTNActionPerformed

