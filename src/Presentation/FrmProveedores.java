    private void SaveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBTNActionPerformed
        // TODO add your handling code here:
        
        //NOMBRE VACIO
        if(SupplierField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Name is required", "Validation", JOptionPane.WARNING_MESSAGE); 
            SupplierField.requestFocusInWindow();
            return;
        }
        
        //TELEFONO VACIO
        if(PhoneField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Phone is required", "Validation", JOptionPane.WARNING_MESSAGE); 
            PhoneField.requestFocusInWindow();
            return;
        }
        
        //DIRECCION VACIA
        if(AddressField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Address is required", "Validation", JOptionPane.WARNING_MESSAGE); 
            AddressField.requestFocusInWindow();
            return;
        }   
        
        String msg = null;
        if(IDField.getText().equals("")){
            DSuppliers dts = new DSuppliers();
            LSuppliers fun = new LSuppliers();

            dts.setName(SupplierField.getText());
            dts.setPhone(PhoneField.getText());
            dts.setAddress(AddressField.getText());
            
            msg = fun.insertSuppliers(dts);
            if(msg == "si"){
               JOptionPane.showMessageDialog(rootPane, "Successfully Registered", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }else
            {
                JOptionPane.showMessageDialog(rootPane, "Uh oh, an error has occurred", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }
        }else{
            DSuppliers dts = new DSuppliers();
            LSuppliers fun = new LSuppliers();
            
            dts.setID(Integer.parseInt(IDField.getText()));
            dts.setName(SupplierField.getText());
            dts.setPhone(PhoneField.getText());
            dts.setAddress(AddressField.getText());
            
            msg = fun.editSuppliers(dts);
            if(msg == "si"){
               JOptionPane.showMessageDialog(rootPane, "Successfully Updated", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }else
            {
                JOptionPane.showMessageDialog(rootPane, "Uh oh, an error has occurred", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }
        }
        cleanfield();
        enablebtn(false);
        showSearch("");
    }//GEN-LAST:event_SaveBTNActionPerformed

