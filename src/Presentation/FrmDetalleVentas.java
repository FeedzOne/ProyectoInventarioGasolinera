    private void GenerateBTN1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerateBTN1ActionPerformed
        // TODO add your handling code here:
        JFileChooser selection = new JFileChooser();
        int op = selection.showSaveDialog(null);
        if (op == JFileChooser.APPROVE_OPTION) {
            String route = selection.getSelectedFile().getAbsolutePath();
            String name = route + IDField.getText() + ".xlsx";
            String reportname = "ReceiptsDetails";
            XSSFWorkbook inventorybook = new XSSFWorkbook();
            XSSFSheet inventoryleaf = inventorybook.createSheet(reportname);

            String[] titles = new String[]{"CODE", "DESCRIPTION", "QUANTITY", "UNIT", "PRICE/UNIT", "TOTAL"};

            Font titlesfont = inventorybook.createFont();
            titlesfont.setBold(true);

            XSSFRow ID = inventoryleaf.createRow(1);
            XSSFCell cellsale1 = ID.createCell(0);
            XSSFCell cellsale2 = ID.createCell(1);
            cellsale1.setCellValue("# SALE: ");
            cellsale2.setCellValue(IDField.getText());
            
            XSSFRow Line = inventoryleaf.createRow(3);
            XSSFCell cellline1 = ID.createCell(0);
            XSSFCell cellline2 = ID.createCell(1);
            cellline1.setCellValue("LINE: ");
            cellline2.setCellValue(LineField.getText());
            
            XSSFRow Supplier = inventoryleaf.createRow(5);
            XSSFCell cellsupplier1 = ID.createCell(0);
            XSSFCell cellsupplier2 = ID.createCell(1);
            cellsupplier1.setCellValue("SUPPLIER: ");
            cellsupplier2.setCellValue(SupplierField.getText());
            
            XSSFRow Date = inventoryleaf.createRow(7);
            XSSFCell celldate1 = ID.createCell(0);
            XSSFCell celldate2 = ID.createCell(1);
            celldate1.setCellValue("DATE: ");
            celldate2.setCellValue(DateField.getText());
            
            XSSFRow title = inventoryleaf.createRow(10);
            for (int i = 0; i < titles.length; i++) {
                XSSFCell cell = title.createCell(i);
                cell.setCellValue(titles[i]);
            }
            int inventoryRow = 11;
            for (int i = 0; i < tblSales.getRowCount(); i++) {
                XSSFRow content = inventoryleaf.createRow(inventoryRow);
                inventoryRow++;
                for (int j = 0; j < tblSales.getColumnCount(); j++) {
                    XSSFCell cell = content.createCell(j);
                    cell.setCellValue(tblSales.getValueAt(i, j).toString());
                }
            }
            inventoryleaf.autoSizeColumn(0);
            inventoryleaf.autoSizeColumn(1);
            inventoryleaf.autoSizeColumn(2);
            inventoryleaf.autoSizeColumn(3);
            inventoryleaf.autoSizeColumn(4);
            inventoryleaf.autoSizeColumn(5);

            try (OutputStream archivo = new FileOutputStream(name)) {
                inventorybook.write(archivo);
                JOptionPane.showMessageDialog(rootPane, "Success", "Info", JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "Uh oh, something went wrong", "Info", JOptionPane.INFORMATION_MESSAGE);
                ex.getStackTrace();
            }
        }
    }//GEN-LAST:event_GenerateBTN1ActionPerformed

