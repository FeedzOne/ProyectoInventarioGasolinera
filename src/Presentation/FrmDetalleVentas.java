    public void setBill(String idfact, String supplier, String line, String regdate) {
        IDField.setText(idfact);
        LineField.setText(line);
        SupplierField.setText(supplier);
        DateField.setText(regdate);
        showbilldetails(Integer.parseInt(idfact));
    }

    private void showbilldetails(int idfact) {
        myModel = (DefaultTableModel) tblSales.getModel();
        LBillsDetails fun = new LBillsDetails();
        DBillsDetails dts = new DBillsDetails();
        dts.setBillsID(idfact);
        myModel = fun.showBillDetails(dts);
        tblSales.setModel(myModel);
        addTotal();
    }

    public void addTotal() {
        DecimalFormat ft = new DecimalFormat("####.00");
        double totalp = 0, total = 0;
        for (int i = 0; i < tblSales.getRowCount(); i++) {
            totalp = Double.parseDouble(tblSales.getValueAt(i, 5).toString());
            total += totalp;
            TotalField.setText(ft.format(total));
        }
    }

